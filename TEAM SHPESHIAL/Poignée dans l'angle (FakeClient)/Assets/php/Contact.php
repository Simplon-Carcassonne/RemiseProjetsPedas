			<p>
				Specialized a été fondé en 1974 par Mike Sinyard, un passionné de cyclisme qui a entrepris de faire le tour d'Europe à vélo, et à la suite duquel il a commencé par importer des composants italiens (Campagnolo, Cinelli) qui étaient difficilement trouvables aux États-Unis1.<br><br>

				L'entreprise commence à produire ses propres composants en 1976, avec la mise sur le marché d'un pneu destiné au vélo de tourisme. Les premiers vélos de la marque arrivent au catalogue en 1979 : le vélo de course Allez et le vélo de tourisme Sequoia.<br><br>

				En 1981, le constructeur marque un changement dans le monde du cyclisme en lançant le premier VTT destiné au grand public : le Stumpjumper. Ce modèle est une forme hybride entre les vélos de tourisme robustes que Mike Sinyard a utilisé pour son tour d'Europe, et les vélos de routes de l'époque. Produit à 500 unités la première année, il s'écoule très rapidement, incitant l'entreprise à anticiper le millésime suivant1. Ce vélo équipé de pneus imposants et de 15 vitesses est si marquant qu'un modèle original est présent dans un musée du Smithsonian Institute.<br><br>

				Devant cette croissance rapide, la marque quitte ses locaux situés dans un entrepôt à San José en 1982 pour un espace bien plus grand à Morgan Hill, toujours dans la banlieue de San Francisco. Ce nouveau pied à terre entouré de vastes espaces propices à la pratique du vélo est idéal pour l'entreprise.<br><br>

				Specialized continue sa progression en créant la première équipe de VTT professionnelle : la Team Stumpjumper en 19832. L'initiative est couronnée de succès, l'équipe gagnant de nombreuses compétitions aux couleurs de la marque, et éprouvant les produits par la même occasion en vue d'une possible future commercialisation. Depuis, l'entreprise n'a cessé de jouer cette carte : des produits technologiquement avancés, testés et éprouvés par des cyclistes professionnels au plus haut niveau de compétition international1.<br>
				La contribution de Mike Sinyard à la pratique du VTT est soulignée en 1988, avec son entrée dans le Mountain Bike Hall of Fame. Cette année est également importante pour Specialized, qui sort le tout premier VTT en fibre de carbone, le Stumpjumper Epic3, composé de 8 tubes de fibre de carbone collés entre eux.<br><br>

				La selle ergonomique Body Geometry fait son apparition et est la toute première selle dont la conception a été spécialement étudiée pour la morphologie masculine. Elle fait suite aux études du Docteur Irwin Goldstein sur la corrélation entre la pratique du cyclisme et l'augmentation du risque d'infertilité masculine, et permet de limiter ce risque en supprimant la pression sur la zone du périnée lors que le cycliste est assis1.<br><br>

				En collaboration avec l'entreprise Duralcan spécialisée dans la conception d'alliages d'aluminium, Specialized sort le tout premier modèle de Jante de vélo conçue en Metal Matrix (Composite à matrice métallique), et produite à 5000 exemplaires. Cette réalisation complexe a nécessité l'implication de nombreux intervenants sur les différents aspects de la production, et a été soulignée par des concurrents comme Huffy qui a avoué avoir travaillé sur l'utilisation de composite à matrice métallique, mais qui a abandonné les recherches faute de parvenir à mettre au point un processus de production à grande échelle.<br>
				Le début des années 1990 marque un renouveau, dans le monde du cyclisme, avec l'arrivée de nombreux concurrents proposant leurs propres gammes de vélos alors que le marché ne suit pas ce rythme d'expansion. À cela se rajoutent des problèmes de gestion et de production qui mettent la marque en difficulté. Alors que beaucoup de concurrents complètent leurs gammes par des modèles low cost vendus en grandes surfaces, Specialized ne se décide à exploiter ce potentiel qu'en 1995, en lançant une sous-marque spécialisée pour l'entrée de gamme et appelée Full Force. Cette annonce a été mal perçue par les revendeurs fidèles à la marque, qui ont perçu cette démarche comme une erreur stratégique1. En 1996, Specialized revient sur sa décision et stoppe le concept Full Force. Mike Sinyard écrit à cette occasion une lettre formelle d'excuse à tous les revendeurs de la marque.<br><br>

				En juin 2001, Merida, le second plus grand fabriquant de cycles au monde, prend part au capital de Specialized. Le pourcentage exact de la participation n'a jamais été révélé.
